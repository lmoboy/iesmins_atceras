    function getRandomWords(words: string[], amount: number): { text: string; correct: string }[] {
        const allLetters: { text: string; correct: string }[] = [];

        for (let i = 0; i < amount; i++) {
            const randomIndex = Math.floor(Math.random() * words.length);
            const word = words[randomIndex];
            word.split('').forEach((char) => {
                allLetters.push({ text: char, correct: 'gray' });
            });
            if (i < amount - 1) {
                allLetters.push({ text: ' ', correct: 'gray' });
            }
        }

        return allLetters;
    }
    if (amount < 1) {
        return getRandomWords(words, words.length - 1);
    } else {
        return getRandomWords(words, amount);
    }
